class Send
{
    //对发送的方法进行封装
    public static function msg($token, $username = 0, $state, $msg, $id = 0)
    {
        return json_encode([
            'token' => $token,
            'username' => $username,
            'state' => $state,//1单聊2群聊
            'msg' => $msg,
            'id' => $id
        ]);
    }
